//4. A partir de um número inteiro, informar se este é par ou ímpar.
class ImparOuPar{
    public static void main(String args[]){
      int numero = 11;
      
      if(numero % 2 == 0){
         System.out.println("O numero e par");
      }
      else{
         System.out.println("O numero e impar");
      }
    }
}