class MenorMenorZero{
    public static void main(String args[]){
        int numero = 0;
          if (numero != 0){
            if (numero < 0){
                 System.out.println("\n" + numero + " e negativo");
            }
            else{
                 System.out.println("\n" + numero + " e positivo");
            }
          }
          else{
            System.out.println("\n E o zero");
          } 
    }
}