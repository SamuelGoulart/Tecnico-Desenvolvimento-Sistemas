class ParaDoisNumeros{
    public static void main(String args[]){
        double  numero = 20;
        double  numero1 = 22;

        if(numero != numero1){
             if(numero > numero1){
                
                System.out.println("\n O primeiro numero inserido e o maior");
             }
             else{

                System.out.println("\n O segundo numero inserido e o maior");
             }
        }
        else{
            System.out.println("\n Os numeros sao iguais");
        }
    }
}