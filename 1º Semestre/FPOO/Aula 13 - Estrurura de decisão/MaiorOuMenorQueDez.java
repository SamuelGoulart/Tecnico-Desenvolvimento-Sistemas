// 2. Para um número qualquer, informar se este é maior ou menor que dez, ou o próprio dez.
class MaiorOuMenorQueDez{
    public static void main(String args[]){
        double numero = -11;

        if(numero > 10){
            System.out.println(numero+" e um numero e maior que dez");
        }
        else{
         System.out.println(numero+" e um numero e menor que dez");
        }
    }
}