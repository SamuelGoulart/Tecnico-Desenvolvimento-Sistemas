// 6. Converter e exibir uma temperatura de graus Celsius para graus Fahrenheit. 
class CelsiusParaFahrenheit{
    public static void main(String argas[]){
        double grauscelsius = 30;
        double fahrenheit = (9 * grauscelsius + 160)/5;
        System.out.println(grauscelsius+" graus celsius em Fahrenheit e "+ fahrenheit);
    }
}